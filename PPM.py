PPM = float(input("Please enter desired concentration change in PPM"))
Gallons = float(input("Please enter number of gallons"))
Litres = (Gallons*3.78541)
PPM_Result_Grams = (PPM*Litres*.001)
PPM_Result_Lbs = (PPM * 0.000008335882 * Gallons)

print ("The result is " +str(PPM_Result_Grams)+ " grams")
print ("The result is " +str(PPM_Result_Lbs)+ "lbs")
